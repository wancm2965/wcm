BOARDINFO *findBoardInfo(Uint16 idBoard)
{
	int i = 0;

	while(boards[i].idBoard != -1)
	{
		if (boards[i].idBoard == idBoard)
			return &boards[i];

		i++;
	}

	return 0;
}

Uint32 CheckBoardClk(void)
{
	Uint32 c;
	Uint32 tim1, tim2;

	tim1 =  _EMIFA_SDTIM_FGET(CNTR);
	asm (" nop");
	tim2 =  _EMIFA_SDTIM_FGET(CNTR);
	c = tim1 - tim2;

#ifdef _DEBUG
	printf("count is %d\n", c);
#endif

	if(c >= 25)
		return SYS_CLK_600MHZ;

	return SYS_CLK_720MHZ;
}

Bool CheckBoardMem(void)
{
	Int col = _EMIFA_SDCTL_FGET(SDCSZ);
	if (col == 0)
		return 1;   // 64M

	return 0;		// 32M
}

